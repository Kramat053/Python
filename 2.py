#Arithmetic Opertors
a = 5
b = 2
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%b)
print(a**b)

#Relational Operators
x = 20
y = 10
print(x==y)
print(x!=y)
print(x>y)
print(x>=y)
print(x<y)
print(x<=y)

#Assignment Operators
num = 100
print(num)
num +=100
print(num)
num *=100
num /=100

#Logical Operators
print(not True)
print(not False)

#Bitwise Operators
g = 2
h = 3
print(g & h)
print(g | h)


#Identity Operators
c = 10
d = 10
print(c is d)
e = [1,2,3,4]
