#Operations on Strings

#Aritmetic Operations
d = "Hello"
e = "World"
print(d+e)
print("*"*10)
print("Hello"*3)

#Relational Operations
"Hello"=="World"
"Hello"!="World"
"Ali">"Meer"
"Hasan"<"Raza"

#Logical Operations
# "" -> False
#"hubbuss" -> True
"Hello" and "World"
"Hello" or "World"
not "Hello"

#Loops on Strings
x = "Pakistan"
for i in x:
  print(i)
for i in x[2:7]:
  print(i)

#Membership Operations
y = "Management"
"M" in y
"y" in y


